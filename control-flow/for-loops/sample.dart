void main() {
  // i++ disebut increment ==> i = i + 1
  // i-- disebut decrement ==> i = i - 1
  // syntax
  // int i = 1 ==> sebuah inisialisasi variable sebagai awalan perulangan
  // i <= 5 ==> sebuah kondisi yang akan terus dijalankan ika bernilai true, dan akan berhenti jika hasil bernilai false
  // i++ ==> aksi yang akan dijalankan selama proses perulangan for berlangsung
  for (int i = 1; i <= 5; i++) {
    print(i);
  }
}

